<article id="raisingheroesbeginning" role="article">
	<p id="author">Jennifer Sukay</p>
		<p class="summary">I decided I wanted to try to make a difference when my 6 year old daughter told me<br>

<p>“Mommy, I don’t ever want to grow up”<br></p>
<p>“Why, honey?”<br></p>
<p>“Because then I have to get a job and it’ll be BORING!”</p>

	<p>Realizing how she must see the adult world- where so many melancholy, sad, bitter, adults unhappy with their professions hang their heads and force
themselves through their days- my heart broke for my little one.  I worried, because in our hometown, most don’t reach for those happy, amazing jobs.
How bleak the future must seem!</p>

	<p>The most inspirational person of my youth was Jane Goodall, who came to speak at my school in Hong Kong.  I loved and still do love animals.
And what a difference she made!  With her love and passion, her stories brought me to tears and filled me with hope of what I could do to help.
Of what power a single person can have.</p>

	<p>“Oh, honey, you can have a job that makes you HAPPY.  You can help animals.  Some people have jobs where they make animals happy,
heal them, save their homes…</p>

	<p>“Really?”<br>

	<p>I still remember how her eyes lighted with hope and wonder.</p>

	<p>“When I was a little girl, a lady came to my school who helped orangutans.   She studied them in the wild and taught them sign language
so they could communicate.   They were being captured and sold as pets.   But when they got bigger the owner didn’t want them anymore
and they no longer had homes.  Babies were taken from their momma’s.  She teaches people to be aware of what is happening and to help
them.”</p>

	<p>I showed her some of Jane Goodall’s videos.</p>

	<p>“You can help animals like this.  Or you can make them feel better.  You can take care of them.  You can do whatever you want and have a
job that makes you happy and that you love doing. “</p>

	<p>Perhaps there’s a child out there who loves computers but who’s city doesn’t give him confidence to create things himself or to reach for a
job he can immerse himself in and enjoy.   Or one who loves dance, or music…</p>

<p>My daughter is 9 years old now and has loved animals from the very beginning.  I feel she will wind up helping them.
But regardless of what she ends up doing, I know she will grow up knowing she can make a difference and have a soul-fulfilling job.</p>

<p>Just like I am here now, eager to help.   Inspiring the youth to reach for the stars.  To look FORWARD to the future.   My soul is happy.</p>
